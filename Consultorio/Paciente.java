package Consultorio;

public class Paciente {
    private int idade;
    private int rg;

    public Paciente(int idade, int rg) {
        this.idade = idade;
        this.rg = rg;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getRg() {
        return rg;
    }

    public void setRg(int rg) {
        this.rg = rg;
    };

    

    
}
